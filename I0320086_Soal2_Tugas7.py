# Fungsi floor
import math
nilai = 77
print ("Rizal RH mempunyai nilai kalkulus sebesar", math.floor(nilai), "pada ujian akhir semester 1")

# Fungsi min dan max
import math
MatTek = 77
Fisika = 87
Biologi = 92
print ("Nilai tertinggi yang Rizal RH dapatkan pada semester ini ialah ", max(MatTek,Fisika,Biologi), ", yaitu pada matkul Biologi")
print ("Nilai terendah yang Rizal RH dapatkan pada semester ini ialah ", min(MatTek,Fisika,Biologi), ", yaitu pada matkul MatTek")

# Fungsi sqrt
import math
Alas = 40
Tinggi = 20
S_miring = Alas ** 2 + Tinggi ** 2
print ("Rizal RH diberi tugas oleh Pak Wayan untuk menghitung sisi miring lapangan fakultas teknik, \nhasil pengamatannya menyatakan bahwa besar sisi miring lapangan fakultas teknik ialah",math.sqrt(S_miring),"m")